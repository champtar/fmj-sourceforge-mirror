package net.sf.fmj.media.protocol.javasound;

/**
 *
 * @author Ken Larson
 *
 */
public class JavaSoundUrlParserException extends Exception
{
    public JavaSoundUrlParserException()
    {
        super();
    }

    public JavaSoundUrlParserException(String message)
    {
        super(message);
    }

    public JavaSoundUrlParserException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public JavaSoundUrlParserException(Throwable cause)
    {
        super(cause);
    }

}
